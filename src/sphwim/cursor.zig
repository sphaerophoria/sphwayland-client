pub fn makeRgba(alloc: std.mem.Allocator) ![]const u8 {
    const ret = try alloc.alloc(u8, width * height * 4);

    for (0..height) |y| {
        for (0..width) |x| {
            const i = y * width + x;
            const d = data[(height - y - 1) * width + x];
            const rgb: u8 = switch (d) {
                0, 1, => 0,
                2 => 255,
                else => unreachable,
            };
            const a: u8 = switch (d) {
                0, => 0,
                1, 2 => 255,
                else => unreachable,
            };

            ret[i * 4 + 0] = rgb;
            ret[i * 4 + 1] = rgb;
            ret[i * 4 + 2] = rgb;
            ret[i * 4 + 3] = a;
        }

    }
    return ret;
}

